create or replace function public.recalculate_worker_balance(target_worker_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if target_worker_id is null then
    return;
  end if;

  update public.workers
  set
    balance = coalesce(
      (
        select sum(case when pt.type = 'credit' then pt.amount else -pt.amount end)
        from public.party_transactions as pt
        where pt.party_type = 'worker'
          and pt.worker_id = target_worker_id
      ),
      0
    ),
    balance_usd = coalesce(
      (
        select sum(case when pt.type = 'credit' then pt.amount_usd else -pt.amount_usd end)
        from public.party_transactions as pt
        where pt.party_type = 'worker'
          and pt.worker_id = target_worker_id
      ),
      0
    ),
    balance_iqd = coalesce(
      (
        select sum(case when pt.type = 'credit' then pt.amount_iqd else -pt.amount_iqd end)
        from public.party_transactions as pt
        where pt.party_type = 'worker'
          and pt.worker_id = target_worker_id
      ),
      0
    )
  where id = target_worker_id;
end;
$$;

create or replace function public.sync_worker_balance_from_party_transactions()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if tg_op in ('UPDATE', 'DELETE') and old.worker_id is not null then
    perform public.recalculate_worker_balance(old.worker_id);
  end if;

  if tg_op in ('INSERT', 'UPDATE') and new.worker_id is not null then
    perform public.recalculate_worker_balance(new.worker_id);
  end if;

  return coalesce(new, old);
end;
$$;

drop trigger if exists trg_sync_worker_balance_from_party_transactions on public.party_transactions;
create trigger trg_sync_worker_balance_from_party_transactions
after insert or update or delete on public.party_transactions
for each row execute function public.sync_worker_balance_from_party_transactions();

create or replace function public.sync_expense_party_transactions()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  credit_amount numeric;
  credit_currency text;
  debit_amount numeric;
  debit_currency text;
  source_date date;
  source_kind text;
  source_title text;
  target_supplier_id bigint;
  target_worker_id bigint;
begin
  if tg_op in ('UPDATE', 'DELETE') then
    delete from public.party_transactions
    where source_invoice_id = old.id
      and source_kind in ('labor_expense', 'supplier_expense');
  end if;

  if tg_op = 'DELETE' then
    return old;
  end if;

  if new.record_status <> 'active' then
    return new;
  end if;

  if new.expense_type = 'labor' and new.labor_worker_id is not null then
    target_worker_id := new.labor_worker_id;
    source_kind := 'labor_expense';
  elsif new.expense_type = 'products' and new.supplier_id is not null then
    target_supplier_id := new.supplier_id;
    source_kind := 'supplier_expense';
  else
    return new;
  end if;

  source_title := coalesce(nullif(new.number, ''), 'Expense');
  source_date := coalesce(new.invoice_date, current_date);
  credit_amount := case
    when coalesce(new.total_amount_usd, 0) > 0 then coalesce(new.total_amount_usd, 0)
    else coalesce(new.total_amount_iqd, 0)
  end;
  credit_currency := case
    when coalesce(new.total_amount_usd, 0) > 0 then 'USD'
    else 'IQD'
  end;
  debit_amount := case
    when coalesce(new.paid_amount_usd, 0) > 0 then coalesce(new.paid_amount_usd, 0)
    else coalesce(new.paid_amount_iqd, 0)
  end;
  debit_currency := case
    when coalesce(new.paid_amount_usd, 0) > 0 then 'USD'
    else 'IQD'
  end;

  if coalesce(new.total_amount_usd, 0) > 0 or coalesce(new.total_amount_iqd, 0) > 0 then
    insert into public.party_transactions (
      party_type,
      worker_id,
      supplier_id,
      project_id,
      type,
      amount,
      currency,
      amount_usd,
      amount_iqd,
      description,
      date,
      source_invoice_id,
      source_kind
    )
    values (
      case when target_worker_id is not null then 'worker' else 'supplier' end,
      target_worker_id,
      target_supplier_id,
      new.project_id,
      'credit',
      credit_amount,
      credit_currency,
      coalesce(new.total_amount_usd, 0),
      coalesce(new.total_amount_iqd, 0),
      case
        when source_kind = 'labor_expense' then 'Labor earned: ' || source_title
        else 'Supplier invoice: ' || source_title
      end,
      source_date,
      new.id,
      source_kind
    );
  end if;

  if coalesce(new.paid_amount_usd, 0) > 0 or coalesce(new.paid_amount_iqd, 0) > 0 then
    insert into public.party_transactions (
      party_type,
      worker_id,
      supplier_id,
      project_id,
      type,
      amount,
      currency,
      amount_usd,
      amount_iqd,
      description,
      date,
      source_invoice_id,
      source_kind
    )
    values (
      case when target_worker_id is not null then 'worker' else 'supplier' end,
      target_worker_id,
      target_supplier_id,
      new.project_id,
      'debit',
      debit_amount,
      debit_currency,
      coalesce(new.paid_amount_usd, 0),
      coalesce(new.paid_amount_iqd, 0),
      case
        when source_kind = 'labor_expense' then 'Labor paid: ' || source_title
        else 'Supplier paid: ' || source_title
      end,
      source_date,
      new.id,
      source_kind
    );
  end if;

  return new;
end;
$$;

