create table if not exists public.party_transactions (
  id bigint generated by default as identity primary key,
  party_type text not null check (party_type in ('worker', 'supplier')),
  worker_id bigint references public.workers(id) on delete cascade,
  supplier_id bigint references public.suppliers(id) on delete cascade,
  project_id bigint references public.projects(id) on delete set null,
  type text not null check (type in ('credit', 'debit')),
  amount numeric(12,2) not null check (amount > 0),
  currency text not null default 'USD' check (currency in ('USD', 'IQD')),
  amount_usd numeric(12,2) not null default 0,
  amount_iqd numeric(12,2) not null default 0,
  description text,
  date date not null default current_date,
  source_invoice_id bigint references public.invoices(id) on delete cascade,
  source_kind text,
  created_at timestamptz not null default now(),
  constraint party_transactions_party_check check (
    (party_type = 'worker' and worker_id is not null and supplier_id is null)
    or (party_type = 'supplier' and supplier_id is not null and worker_id is null)
  ),
  constraint party_transactions_dual_currency_positive check (
    amount_usd >= 0
    and amount_iqd >= 0
    and (amount_usd > 0 or amount_iqd > 0)
  )
);

create index if not exists idx_party_transactions_party
  on public.party_transactions(party_type, worker_id, supplier_id);
create index if not exists idx_party_transactions_worker_id
  on public.party_transactions(worker_id);
create index if not exists idx_party_transactions_supplier_id
  on public.party_transactions(supplier_id);
create index if not exists idx_party_transactions_project_id
  on public.party_transactions(project_id);
create index if not exists idx_party_transactions_date
  on public.party_transactions(date);
create index if not exists idx_party_transactions_source_invoice_id
  on public.party_transactions(source_invoice_id);
create unique index if not exists idx_party_transactions_invoice_source
  on public.party_transactions(source_invoice_id, party_type, type)
  where source_kind in ('labor_expense', 'supplier_expense');

