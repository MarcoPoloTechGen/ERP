create or replace function public.get_dashboard_overview()
returns jsonb
language sql
stable
security invoker
set search_path = public
as $$
with visible_projects as (
  select id, name, status
  from public.projects
),
visible_invoices as (
  select
    id,
    number,
    status,
    supplier_name,
    project_name,
    project_id,
    total_amount,
    paid_amount,
    remaining_amount
  from public.app_invoices
),
visible_workers as (
  select id, name, role, balance
  from public.workers
),
visible_transactions as (
  select worker_id, type, amount
  from public.worker_transactions
),
project_summaries as (
  select
    p.id,
    p.name,
    p.status,
    count(i.id)::int as invoice_count,
    coalesce(sum(i.total_amount), 0)::numeric(12,2) as total_invoiced,
    coalesce(sum(i.paid_amount), 0)::numeric(12,2) as total_paid,
    coalesce(sum(i.remaining_amount), 0)::numeric(12,2) as remaining
  from visible_projects as p
  left join visible_invoices as i
    on i.project_id = p.id
  group by p.id, p.name, p.status
),
worker_summaries as (
  select
    w.id,
    w.name,
    w.role,
    w.balance,
    coalesce(sum(case when wt.type = 'credit' then wt.amount else 0 end), 0)::numeric(12,2) as total_credit,
    coalesce(sum(case when wt.type = 'debit' then wt.amount else 0 end), 0)::numeric(12,2) as total_debit
  from visible_workers as w
  left join visible_transactions as wt
    on wt.worker_id = w.id
  group by w.id, w.name, w.role, w.balance
),
invoice_summaries as (
  select
    id,
    number,
    status,
    supplier_name,
    project_name,
    total_amount,
    paid_amount,
    remaining_amount as remaining
  from visible_invoices
)
select jsonb_build_object(
  'totalWorkers',
  (select count(*) from visible_workers),
  'activeProjects',
  (select count(*) from visible_projects where status = 'active'),
  'totalSuppliers',
  (select count(*) from public.suppliers),
  'invoicesUnpaid',
  (select count(*) from visible_invoices where status <> 'paid'),
  'totalInvoiceAmount',
  (select coalesce(sum(total_amount), 0) from visible_invoices),
  'totalPaidAmount',
  (select coalesce(sum(paid_amount), 0) from visible_invoices),
  'remainingAmount',
  (select coalesce(sum(remaining_amount), 0) from visible_invoices),
  'projectsSummary',
  coalesce(
    (
      select jsonb_agg(
        jsonb_build_object(
          'id', ps.id,
          'name', ps.name,
          'status', ps.status,
          'invoiceCount', ps.invoice_count,
          'totalInvoiced', ps.total_invoiced,
          'totalPaid', ps.total_paid,
          'remaining', ps.remaining
        )
        order by ps.remaining desc, ps.name asc
      )
      from project_summaries as ps
    ),
    '[]'::jsonb
  ),
  'workersSummary',
  coalesce(
    (
      select jsonb_agg(
        jsonb_build_object(
          'id', ws.id,
          'name', ws.name,
          'role', ws.role,
          'balance', ws.balance,
          'totalCredit', ws.total_credit,
          'totalDebit', ws.total_debit
        )
        order by abs(ws.balance) desc, ws.name asc
      )
      from worker_summaries as ws
    ),
    '[]'::jsonb
  ),
  'invoicesSummary',
  coalesce(
    (
      select jsonb_agg(
        jsonb_build_object(
          'id', inv.id,
          'number', inv.number,
          'status', inv.status,
          'supplierName', inv.supplier_name,
          'projectName', inv.project_name,
          'totalAmount', inv.total_amount,
          'paidAmount', inv.paid_amount,
          'remaining', inv.remaining
        )
        order by inv.remaining desc, inv.number asc
      )
      from invoice_summaries as inv
    ),
    '[]'::jsonb
  )
);
$$;

