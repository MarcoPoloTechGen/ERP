create table if not exists public.invoice_history (
  id bigint generated by default as identity primary key,
  invoice_id bigint not null references public.invoices(id) on delete cascade,
  change_type text not null check (change_type in ('created', 'updated')),
  number text not null,
  supplier_id bigint references public.suppliers(id) on delete set null,
  supplier_name text,
  project_id bigint references public.projects(id) on delete set null,
  project_name text,
  building_id bigint references public.project_buildings(id) on delete set null,
  building_name text,
  product_id bigint references public.products(id) on delete set null,
  product_name text,
  total_amount numeric(12,2) not null default 0 check (total_amount >= 0),
  paid_amount numeric(12,2) not null default 0 check (paid_amount >= 0 and paid_amount <= total_amount),
  currency text not null default 'USD' check (currency in ('USD', 'IQD')),
  status text not null default 'unpaid' check (status in ('unpaid', 'partial', 'paid')),
  invoice_date date,
  due_date date,
  notes text,
  image_url text,
  changed_by uuid references public.profiles(id) on delete set null,
  changed_by_name text,
  changed_at timestamptz not null default now()
);

