create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  assigned_role text := 'user';
begin
  if not exists (select 1 from public.profiles) then
    assigned_role := 'admin';
  end if;

  insert into public.profiles (id, email, full_name, role)
  values (new.id, new.email, coalesce(new.raw_user_meta_data->>'full_name', ''), assigned_role)
  on conflict (id) do update
  set
    email = excluded.email,
    full_name = coalesce(excluded.full_name, public.profiles.full_name);

  return new;
end;
$$;

create or replace function public.current_user_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(
    (
      select p.role
      from public.profiles as p
      where p.id = auth.uid()
    ),
    'user'
  );
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select auth.role() = 'authenticated' and public.current_user_role() = 'admin';
$$;

create or replace function public.can_access_project(target_project_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    auth.role() = 'authenticated'
    and (
      target_project_id is null
      or public.is_admin()
      or exists (
        select 1
        from public.project_memberships as pm
        where pm.project_id = target_project_id
          and pm.user_id = auth.uid()
      )
    );
$$;

create or replace function public.can_access_invoice_object(object_name text)
returns boolean
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  object_prefix text;
  project_id_text text;
begin
  if auth.role() <> 'authenticated' then
    return false;
  end if;

  object_prefix := split_part(coalesce(object_name, ''), '/', 1);

  if object_prefix = 'shared' then
    return true;
  end if;

  if object_prefix ~ '^[0-9]{4}$' then
    return true;
  end if;

  if object_prefix like 'project-%' then
    project_id_text := nullif(replace(object_prefix, 'project-', ''), '');
    return public.can_access_project(project_id_text::bigint);
  end if;

  return false;
exception
  when others then
    return false;
end;
$$;

create or replace function public.sync_worker_balance()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  target_worker_id bigint;
begin
  target_worker_id := coalesce(new.worker_id, old.worker_id);

  update public.workers
  set balance = coalesce(
    (
      select sum(case when wt.type = 'credit' then wt.amount else -wt.amount end)
      from public.worker_transactions as wt
      where wt.worker_id = target_worker_id
    ),
    0
  )
  where id = target_worker_id;

  return coalesce(new, old);
end;
$$;

create or replace function public.replace_project_buildings(
  p_project_id bigint,
  p_building_names text[]
)
returns void
language plpgsql
security invoker
set search_path = public
as $$
declare
  cleaned_names text[];
begin
  if not public.is_admin() then
    raise exception 'Only administrators can update project buildings.';
  end if;

  cleaned_names := array(
    select distinct trimmed_name
    from (
      select nullif(btrim(name), '') as trimmed_name
      from unnest(coalesce(p_building_names, array[]::text[])) as name
    ) as prepared_names
    where trimmed_name is not null
    order by trimmed_name
  );

  delete from public.project_buildings
  where project_id = p_project_id;

  if coalesce(array_length(cleaned_names, 1), 0) = 0 then
    return;
  end if;

  insert into public.project_buildings (project_id, name)
  select p_project_id, name
  from unnest(cleaned_names) as name;
end;
$$;

create or replace function public.save_project_with_buildings(
  p_id bigint,
  p_name text,
  p_client text,
  p_location text,
  p_status text,
  p_budget numeric,
  p_start_date date,
  p_end_date date,
  p_building_names text[]
)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  target_id bigint;
begin
  if not public.is_admin() then
    raise exception 'Only administrators can create or update projects.';
  end if;

  if nullif(btrim(p_name), '') is null then
    raise exception 'Project name is required.';
  end if;

  if p_budget is not null and p_budget < 0 then
    raise exception 'Project budget cannot be negative.';
  end if;

  if p_end_date is not null and p_start_date is not null and p_end_date < p_start_date then
    raise exception 'Project end date cannot be earlier than the start date.';
  end if;

  if p_id is null then
    insert into public.projects (
      name,
      client,
      location,
      status,
      budget,
      start_date,
      end_date
    )
    values (
      btrim(p_name),
      nullif(btrim(p_client), ''),
      nullif(btrim(p_location), ''),
      coalesce(nullif(btrim(p_status), ''), 'active'),
      p_budget,
      p_start_date,
      p_end_date
    )
    returning id into target_id;
  else
    update public.projects
    set
      name = btrim(p_name),
      client = nullif(btrim(p_client), ''),
      location = nullif(btrim(p_location), ''),
      status = coalesce(nullif(btrim(p_status), ''), 'active'),
      budget = p_budget,
      start_date = p_start_date,
      end_date = p_end_date
    where id = p_id
    returning id into target_id;

    if target_id is null then
      raise exception 'Project not found.';
    end if;
  end if;

  perform public.replace_project_buildings(target_id, p_building_names);

  return target_id;
end;
$$;

create or replace function public.replace_user_project_memberships(
  p_user_id uuid,
  p_project_ids bigint[]
)
returns void
language plpgsql
security invoker
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception 'Only administrators can update project memberships.';
  end if;

  delete from public.project_memberships
  where user_id = p_user_id;

  if coalesce(array_length(p_project_ids, 1), 0) = 0 then
    return;
  end if;

  insert into public.project_memberships (user_id, project_id)
  select p_user_id, distinct_project_id
  from (
    select distinct project_id as distinct_project_id
    from unnest(p_project_ids) as project_id
  ) as distinct_projects
  where exists (
    select 1
    from public.projects as p
    where p.id = distinct_projects.distinct_project_id
  );
end;
$$;

