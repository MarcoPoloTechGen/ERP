create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
as $$
declare
  assigned_role text := 'user';
begin
  if not exists (select 1 from public.profiles) then
    assigned_role := 'admin';
  end if;

  insert into public.profiles (id, email, full_name, role)
  values (new.id, new.email, coalesce(new.raw_user_meta_data->>'full_name', ''), assigned_role)
  on conflict (id) do update
  set email = excluded.email,
      full_name = coalesce(excluded.full_name, public.profiles.full_name);

  return new;
end;
$$;

create or replace function public.sync_worker_balance()
returns trigger
language plpgsql
as $$
declare
  target_worker_id bigint;
begin
  target_worker_id := coalesce(new.worker_id, old.worker_id);

  update public.workers
  set balance = coalesce((
    select sum(case when wt.type = 'credit' then wt.amount else -wt.amount end)
    from public.worker_transactions wt
    where wt.worker_id = target_worker_id
  ), 0)
  where id = target_worker_id;

  return coalesce(new, old);
end;
$$;

drop trigger if exists trg_sync_worker_balance on public.worker_transactions;
create trigger trg_sync_worker_balance
after insert or update or delete on public.worker_transactions
for each row execute function public.sync_worker_balance();

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

insert into public.profiles (id, email, full_name, role)
select
  users.id,
  users.email,
  coalesce(users.raw_user_meta_data->>'full_name', ''),
  case
    when row_number() over (order by users.created_at asc, users.id asc) = 1 then 'admin'
    else 'user'
  end
from auth.users as users
on conflict (id) do update
set
  email = excluded.email,
  full_name = coalesce(excluded.full_name, public.profiles.full_name),
  role = case when public.profiles.role = 'admin' then 'admin' else excluded.role end;

